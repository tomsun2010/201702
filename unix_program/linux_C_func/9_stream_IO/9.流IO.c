FILE * fopen(const char * path,const char * mode);
/*************************************************
 * 功能说明： fopen(打开文件)
 * 表头文件： #include<stdio.h>
 * 定义函数： FILE * fopen(const char * path,const char * mode);
 * 函数说明： path字符串是文件路径及文件名
              mode字符串则代表着流形态，mode有下列几种形态字符串：
				    r   打开只读文件，该文件必须存在。
					r+  打开可读写的文件，该文件必须存在。
					w   打开只写文件，若文件存在则文件长度清为0，即该文件内容会消失。若文件不存在则建立该文件。
					w+  打开可读写文件，若文件存在则文件长度清为零，即该文件内容会消失。若文件不存在则建立该文件。
					a   以附加的方式打开只写文件。若文件不存在，则会建立该文
					    件，如果文件存在，写入的数据会被加到文件尾，即文件原先的内
					    容会被保留。
					a+  以附加方式打开可读写的文件。若文件不存在，则会建立该文
					    件，如果文件存在，写入的数据会被加到文件尾后，即文件原先的
					    内容会被保留。
						
					上述的形态字符串都可以再加一个b 字符，如rb、w+b 或ab＋等组
					合，加入b字符用来告诉函数库打开的文件为二进制文件，而非纯文字文件
 * 返回值  ： 文件顺利打开后，指向该流的文件指针就会被返回。若果文件打开
              失败则返回NULL，并把错误代码存在errno中。
 * 附加说明： 一般而言，开文件后会作一些文件读取或写入的动作，若开文件失
              败，接下来的读写动作也无法顺利进行，所以在fopen（）后请作错误判断及处理。

	    例子： FILE *fp = fopen("xxx", "r");   //只读
			   FILE *fp = fopen("xxx", "w+");  //打开并清空可读写文件
			   FILE *fp = fopen("xxx", "wb");
**************************************************/

