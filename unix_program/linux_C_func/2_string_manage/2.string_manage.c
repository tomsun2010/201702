int strcoll( const char * str1, const char * str2 );
/*************************************************
 * 功能说明： 根据环境变量LC_COLLATE来比较字符串
 * 表头文件： #include <string.h>
 * 定义函数： int strcoll( const char * str1, const char * str2 );
 * 函数说明： strcoll() 会依环境变量 LC_COLLATE 所指定的文字排列次序来比较 s1 和 s2 字符串。
              默认情况下，LC_COLLATE 为"POSIX"或"C"，strcoll() 和 strcmp() 一样根据ASCII比较字符串大小。
              对于设置了 LC_COLLATE 语言环境的情况下，则根据 LC_COLLATE 设置的语言排序方式进行比较。
			  例如，汉字会根据拼音进行比较。
 
 * 返回值  ： 若字符串 str1 和 str2 相同则返回0。
              若 str1 大于 str2 则返回大于 0 的值，否则返回小于 0 的值。
 * 附加说明：  
***************************************************************/

int strcmp(const char *s1, const char *s2);
/*************************************************
 * 功能说明： 比较字符串
 * 表头文件： #include <string.h>
 * 定义函数： int strcmp(const char *s1, const char *s2);
 * 函数说明： 字符串大小的比较是以ASCII 码表上的顺序来决定，此顺序亦为字符的值。
              strcmp()首先将s1 第一个字符值减去s2 第一个字符值，若差值为0 则再继续比较下个字符，若差值不为0 则将差值返回。
			  例如字符串"Ac"和"ba"比较则会返回字符"A"(65)和'b'(98)的差值(－33)。
 
 * 返回值  ： 若字符串 str1 和 str2 相同则返回0。
              若 str1 大于 str2 则返回大于 0 的值，否则返回小于 0 的值。
 * 附加说明：  
***************************************************************/

int strncmp ( const char * str1, const char * str2, size_t n );
/*************************************************
 * 功能说明： 比较字符串前n个字符
 * 表头文件： #include <string.h>
 * 定义函数： int strncmp ( const char * str1, const char * str2, size_t n );
 * 函数说明： 字符串大小的比较是以ASCII 码表上的顺序来决定，此顺序亦为字符的值。
              strcmp()首先将s1 第一个字符值减去s2 第一个字符值，若差值为0 则再继续比较下个字符，若差值不为0 则将差值返回。
 
 * 返回值  ： 若字符串 str1 和 str2 前n个字符相同，则返回0。
              若str1大于str2则返回大于0的值，否则返回小于0的值。
 * 附加说明： 要比较的字符包括字符串结束标志'\0'，而且一旦遇到'\0'就结束比较，
              无论n是多少，不再继续比较后边的字符。
***************************************************************/

