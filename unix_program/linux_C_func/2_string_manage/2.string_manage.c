char * strrchr(const char *str, int c);
/*************************************************
 * 功能说明： 查找某字符在字符串中最后一次出现的位置
 * 表头文件： #include <string.h>
 * 定义函数： char * strrchr(const char *str, int c);
 * 函数说明： str 为要查找的字符串，c 为要查找的字符。
			  strrchr() 将会找出 str 字符串中最后一次出现的字符 c 的地址，然后将该地址返回。
 
 * 返回值  ： 如果找到就返回该字符最后一次出现的位置，否则返回 NULL。
              返回的地址是字符串在内存中随机分配的地址再加上你所搜索的字符在字符串位置。
			  设字符在字符串中首次出现的位置为 i，那么返回的地址可以理解为 str + i。
 * 附加说明： 如果希望查找某字符在字符串中第一次出现的位置，可以使用 strchr() 函数。
***************************************************************/

 char * strchr (const char *str, int c);
